package colors;

/**
 * The {@code Colors} class provides ANSI escape codes for coloring text output in the console.
 * Each color is represented by a static final string that can be used to change the color of text output.
 */
public class Colors {

    /**
     * ANSI reset code to reset text formatting.
     */
    public static final String ANSI_RESET = "\u001B[0m";
    /**
     * ANSI black color code.
     */
    public static final String ANSI_BLACK = "\u001B[30m";
    /**
     * ANSI red color code.
     */
    public static final String ANSI_RED = "\u001B[31m";
    /**
     * ANSI green color code.
     */
    public static final String ANSI_GREEN = "\u001B[32m";
    /**
     * ANSI yellow color code.
     */
    public static final String ANSI_YELLOW = "\u001B[33m";
    /**
     * ANSI blue color code.
     */
    public static final String ANSI_BLUE = "\u001B[34m";
    /**
     * ANSI purple color code.
     */
    public static final String ANSI_PURPLE = "\u001B[35m";
    /**
     * ANSI cyan color code.
     */
    public static final String ANSI_CYAN = "\u001B[36m";
    /**
     * ANSI white color code.
     */
    public static final String ANSI_WHITE = "\u001B[37m";
}
