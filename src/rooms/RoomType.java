package rooms;

/**
 * Enum representing different types of rooms in the castle.
 */
public enum RoomType {
    /**
     * The kitchen room type.
     */
    KITCHEN,

    /**
     * The bedroom room type.
     */
    BEDROOM,

    /**
     * The bathroom room type.
     */
    BATHROOM,

    /**
     * The living room type.
     */
    LIVINGROOM,

    /**
     * The gym room type.
     */
    GYM,

    /**
     * The office room type.
     */
    OFFICE;
}
